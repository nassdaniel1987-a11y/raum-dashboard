			</button>

			<button 
				class="icon-button edit-button" 
				title="Bearbeiten" 
				onclick={(e) => { e.stopPropagation(); onEdit(room); }}
			>
				‚úèÔ∏è
			</button>

			<!-- ‚úÖ NEU: L√∂sch-Button direkt sichtbar -->
			<button 
				class="icon-button delete-button" 
				title="L√∂schen" 
				onclick={handleDelete}
			>
				üóëÔ∏è
			</button>
		</div>
	{/if}

	<!-- Status Badge -->
	<div class="status-badge" class:open={room.isOpen}>
		{#if room.isOpen}
			<span in:scale={{ duration: 300 }}>‚úì</span>
		{:else}
			<span in:scale={{ duration: 300 }}>üîí</span>
		{/if}
	</div>

	{#if displayTime && !room.isOpen}
		<div class="time-badge-top">
			üïê √ñffnet um {displayTime}
		</div>
	{/if}

	<div
		class="card-content"
		onclick={handleClick}
	>
		<h3 class="room-title" style="font-size: {titleFontSize}px;">{room.name}</h3>

		{#if room.config?.activity}
			<p class="room-activity" style="font-size: {textFontSize}px;">{room.config.activity}</p>
		{/if}
	</div>

	{#if !room.isOpen}
		<div class="lock-overlay" transition:fade={{ duration: 300 }}>
			<div class="lock-icon">üîí</div>
		</div>
	{/if}
</div>

